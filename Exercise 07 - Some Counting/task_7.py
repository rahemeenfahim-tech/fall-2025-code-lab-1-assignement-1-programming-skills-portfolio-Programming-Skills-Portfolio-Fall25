print("A loop that counts up from 0 to 50 in increments of 1")
for y in range(0,51):                                                   #A loop that counts up from 0 till it reaches 50
    print(y)
    
    
print("A loop that counts down from 50 to 0 in decrements of 1")    
for y in range(50,-1,-1):                                               #A loop that counts down from 50 till it reaches 0
    print(y)
    

print("A loop that counts up from 30 to 50 in increments of 1")       
for y in range(30,51):                                                  #A loop that counts down from 50 till it reaches 0
    print(y)
    

print("A loop that counts down from 50 to 10 in decrements of 2")      
for y in range(50,9,-2):                                                #A loop that counts down from 50 till it reaches 10 in decrements of 2 
    print(y)
    

